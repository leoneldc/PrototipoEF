    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (pr.isNumeric(jTextField1.getText(), jTextField3.getText(), jTextField6.getText())) {
            if (pr.isNoneEmpty(jTextField1, jTextField2, jTextField3, jTextField5, jTextField6)) {
                if (pr.isSelected(jRadioButton1, jRadioButton2)) {
                    ClienteDAO dao = new ClienteDAO();
                    vcliente.setCodigo(jTextField1.getText());
                    vcliente.setNombre(jTextField2.getText());
                    vcliente.setDpi(jTextField3.getText());
                    vcliente.setCorreo(jTextField5.getText());
                    vcliente.setTel(jTextField6.getText());
                    if (jRadioButton1.isSelected()) {
                        vcliente.setEstado("1");
                    } else {
                        vcliente.setEstado("0");
                    }
                    dao.insert(vcliente);
                    actualizarTabla();
                    bitacora.GuardarEnBitacora("insertar", "2002");
                    limpiar();
                    pr.AlertaMensaje("guardado", "Cliente", "exitosamente");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

