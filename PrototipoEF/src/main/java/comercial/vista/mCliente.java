    void actualizarTabla() {
        ProcesosRepetidos pr = new ProcesosRepetidos();
        String columnas[] = {"ID", "NOMBRE", "DPI", "CORREO", "TEL", "ESTADO"};
        int cantidadcolumnas = columnas.length;
        pr.llenarColumnas(columnas, cantidadcolumnas, jTable1);
        String datos[] = new String[cantidadcolumnas];
        int tamaño[] = {50, 200, 200, 200, 200, 150};
        ClienteDAO dao = new ClienteDAO();
        List<Clientes> clientes = dao.select();
        for (Clientes cliente : clientes) {
            datos[0] = cliente.getCodigo();
            datos[1] = cliente.getNombre();
            datos[2] = cliente.getDpi();
            datos[3] = cliente.getCorreo();
            datos[4] = cliente.getTel();
            if (cliente.getEstado().equals("1")) {
                datos[5] = "Activo";
            } else {
                datos[5] = "Inactivo";
            }
            pr.llenarFilas(datos, tamaño, jTable1);
        }
    }

